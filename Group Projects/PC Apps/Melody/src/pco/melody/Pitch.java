package pco.melody;

/**
 * Note type enumeration.
 * 
 * @author Eduardo R. B. Marques, DI/FCUL, 2015
 *
 */
public enum Pitch {
  /** A ("Lá") */
  A,
  /** B ("Si") */
  B,
  /** C ("Dó") */
  C,
  /** D ("Ré") */
  D,
  /** E ("Mi") */
  E,
  /** E ("Fá") */
  F,
  /** F ("Sol") */
  G,
  /** Special constant for silence. */
  S;
}
